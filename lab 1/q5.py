# Using a for loop to print numbers from 1 to 10
print("Numbers using for loop:")
for i in range(1, 11):
    print(i)

# Using a while loop to print numbers from 1 to 10
print("\nNumbers using while loop:")
counter = 1  # Start from 1
while counter <= 10:  # Continue until 10
    print(counter)
    counter += 1
